function avatar(seed: string) {
  return `https://i.pravatar.cc/128?u=${encodeURIComponent(seed)}`
}

function reviewsFor(driverId: string, base: Omit<DriverReview, 'id'>[]): DriverReview[] {
  return base.map((r, i) => ({ ...r, id: `${driverId}-R-${i}` }))
}

export function deliveriesTemplate(driverId: string): DriverDelivery[] {
  const vendors = ['Spice Route Kitchen', 'Urban Bites', 'FreshMart Express', 'Cafe Noir', 'Thai Garden']
  const customers = ['Sara K.', 'James M.', 'Nadia R.', 'Omar H.', 'Priya S.', 'Leo T.']
  const rows: DriverDelivery[] = []
  for (let i = 0; i < 12; i++) {
    const day = Math.max(1, 20 - Math.floor(i / 2))
    let st: DriverDelivery['status'] = 'completed'
    if (i === 0) st = 'in_transit'
    else if (i % 9 === 0) st = 'cancelled'
    else if (i % 11 === 0) st = 'failed'
    rows.push({
      id: `D-${driverId}-${100 + i}`,
      customer: customers[i % customers.length],
      vendor: vendors[i % vendors.length],
      deliveryFee: 2.5 + (i % 5) * 0.75,
      distanceKm: 1.2 + (i % 7) * 0.6,
      status: st,
      completedAt:
        st === 'completed'
          ? `2026-05-${String(day).padStart(2, '0')} ${String(14 + (i % 4)).padStart(2, '0')}:${String((i * 7) % 60).padStart(2, '0')}:00`
          : null,
      date: `2026-05-${String(day).padStart(2, '0')}`,
    })
  }
  return rows
}

export function weekSeries(seed: number): EarningsPoint[] {
  return ['W1', 'W2', 'W3', 'W4'].map((label, i) => ({
    label,
    amount: Math.round(180 + seed * 17 + i * 42 + (i % 3) * 20),
  }))
}

export function monthSeries(seed: number): EarningsPoint[] {
  return ['Jan', 'Feb', 'Mar', 'Apr', 'May'].map((label, i) => ({
    label,
    amount: Math.round(1200 + seed * 40 + i * 180 + (i % 2) * 90),
  }))
}

export function growthSeries(seed: number): GrowthPoint[] {
  return ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat', 'Sun'].map((label, i) => ({
    label,
    count: Math.max(0, Math.round(4 + (seed % 5) + i * 0.8 + (i % 2) * 3)),
  }))
}

