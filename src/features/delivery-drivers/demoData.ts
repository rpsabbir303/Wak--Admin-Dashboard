export function computeOverviewStats(drivers: DeliveryDriver[]) {
  const activeDrivers = drivers.filter((d) => d.accountStatus === 'active').length
  const pendingApprovals = drivers.filter((d) => d.accountStatus === 'pending').length
  const onlineDrivers = drivers.filter(
    (d) => d.accountStatus === 'active' && (d.liveStatus === 'online' || d.liveStatus === 'delivering'),
  ).length
  const completedDeliveriesToday = drivers.reduce((s, d) => s + d.completedToday, 0)
  const totalDriverEarnings = drivers.reduce((s, d) => s + d.totalEarnings, 0)
  return {
    totalDrivers: drivers.length,
    activeDrivers,
    pendingApprovals,
    onlineDrivers,
    completedDeliveriesToday,
    totalDriverEarnings,
  }
}
